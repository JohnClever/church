                                    <h2 class="sc_title sc_title_regular">History</h2>
                                    <p>
                                        <b>
                                            The International Central Gospel Church- University of Cape Coast Campus
                                            Ministry-ICGC-UCC-CM is an Evangelical, Charismatic Christian Church. It 
                                            was officially inaugurated as a campus ministry on the 24th of April, 2005, 
                                            in University of Cape Coast, Cape Coast. The first meeting was held in the 
                                            VOTEC Dinning Hall, old site with an initial membership of over 130 students.
                                        </b>    
                                    </p>
                                    <p>
                                            The dinning hall became our regular meeting place. We met there for six months
                                            until one day, after church service, we were asked not to fellowship on campus the
                                            next Sunday. That was on the 30th of October, 2005. We were not even allowed to meet
                                            with our members to inform them of our movement. But God being so good, He gave us the 
                                            Cape Coast Workers College.
                                            Although the Campus Ministry was inaugurated on the 25th of April, 2005, it went through 
                                            several phase of challenges.
                                    </p>
                                    <p>
                                            Since the foundation of the world, God has used men to achieve things on earth.
                                            The revelation to establish an ICGC_CM in UCC was given to our campus coordinator, Rev. 
                                            Reuben Obeng but because he wasn't a student on campus but was pastoring one of the ICGC
                                            churches in Elmina, in the Central Region, God located a Nobel man with a willing heart who
                                            was ready to sacrifice at all cost to lay foundation with a virtuous woman who eventually became
                                            the president and vice president respectively in the persons of Mr. Wisdom Agbasa and Ms Vida Koranteng.
                                            Announcement was made in Pentefel for all members of ICGC to meet briefly after service for some important 
                                            information. Similar Announcement was sent to Light House Chapel International but they refused to make it.
                                            The information was that, they would like to meet as family to be praying together as a family and they agreed.
                                            In the evening of that Sunday, 16th October, 2004 at 6:00pm fourteen(14) people met at the basket ball court 
                                            at old site to pray in the name of ICGC. Rev. Reuben Obeng was at the meeting and gave an exhortation.
                                    </p>
                                    <p>
                                            The following Sunday evening, 23rd October, 2004 eleven(11) people were present at the meeting after which Rev. Obeng advised 
                                            that a constitution should be prepared in order to enable them to be registered on campus. An interim committee was formed to 
                                            facilitate th registration. There was a need for a patron therefore Dr. L.K Sam-Amoah, a senior lecturer at the Agriculture Enginneering
                                            Department at the University of Cape Coast who also happened to be an ICGC member at the Exalted Temple, Cape Coast accepted the
                                            offer and he is still the patron with Dr. Daniel Okae-Anti who was added August, 2010/2011 Academic year. As prayers were being made 
                                            on every Sunday seeking God's face the doors of the PCC now the PPCC and the chaplaincy were also not left behind. It is by registration
                                            that is when the campus ministry can be recognize and be free to operate freely as a church without restrictions.
                                    </p>
                                    <p>
                                            This is where a lot of opposition rose in even from the members of ICGC who found themselves in executive positions in other churches on campus especially pentefel.
                                            With the reason that our establishment will cause the collapse of the Pentecostal Students Fellowship. Inspite of all these hindrances, prayers were still 
                                            being made and out of it God brought a good samaritan, a member of the chaplaincy who gave glimpse of hope saying if we start to operate as a church,
                                            he will pretend not to have seen us and as time goes on we may be registered. With these words, a series meeting was held to plan for the first Sunday service. It was 
                                            not easy to get a venue to fellowship as well, all efforts to get one of the lecture theatres failed. With the help of the patron, Dr L.K Sam-Amoah,
                                            the VOTEC dinning hall was given at GH25.00 per meeting. Others were also tasked to publicize the service. Chairs were rented and instruments were borrowed from Grace Temple 
                                            and Elmina ICGC in the Central Region. Through out prepaarations some skeptics did not believe that it will be possible for it to be done.
                                            One of them was Rev. Kingsley Akwasi Prempeh, the former District Supervising Minister(DSM) for Cape Coast, Central Region. This he confessed when he came to preach 
                                            to inaugurate the ministry; he had to change his sermon at the pulpit.  
                                    </p>
                                    <p>
                                            After the ejection on the 30th of October, 2005, the ministry has to relocate to the Cape Coast Workers College. Therefore the executives went there on the next day which was 31st October, 2005 to book the place.
                                            It was one of their class rooms. It was announce on the 2nd of November, 2010 at Wednesday's prayer meeting that the fellowship place has been relocated. The news spread rapidly, and because 
                                            of the venue, bus has to convey the members to service on Sundays. As the good book makes it clear to us each day, that the vision is for appointed time, though it tarry yet it shall come to pass.
                                            And though our outward man perishes yet our in ward man is being renewed day by day.
                                    </p>
                                    <p>
                                            In the year that the General Overseer declared as the year of the BREAKTHROUGH and the ABUNDANCE of GRACE because of the ministry's fifth anniversary there was a 
                                            manifestation of the power of God through which the campus ministry relocated to the University community once again, for the first time 
                                            after the ejection from the VOTEC dinning hall at old site. Because the official venue wasn't given yet, the Sasakawa conference room 
                                            was used for service on the 22nd of August, 2010 of which ninty (90) members were in attendance, fifty three were freshmen but continuing students had not 
                                            fully reported to campus by then. The next Sunday 29th August, 2010 was joint service organized by the Prostestant and Pentecostal Chaplaincy Committee (PPCC) so therefore
                                            therefore there was no individual service. The BREAKTROUGH actually manifested on the 5th of September, 2010. This was on the 5th day 
                                            after the 5th month of the inauguration in 2005 in the 5th year 2010 (555). The attendance was 115. The actual membership of the campus ministry as at when this document is being prepared is 206.
                                            The Campus Ministry of the International Central Gospel Church, now fellowship on the University of Cape Coast Campus at the Cafeteria Roof Top adjacent to the Casely Hayford Hall.
                                    </p>
                                </section>
